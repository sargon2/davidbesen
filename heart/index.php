2/11/09: I'm doing well.  My atrial fibrillation has pretty much completely stopped.  My bones have mended to the point that I can open doors, drive, etc.  I had some complications with my incision, so I'm on antibiotics, but it's healing fine now.  I'll probably be back to work next week.
<br />
<br />
Interesting facts:<br />
 - I lost about 13 pounds from the surgery<br />
 - I was on the heart/lung machine, so they deflated my lungs.  My heart was stopped for 37 minutes<br />
 - The pathology report confirmed 100% that the tumor was an "atrial myxoma - left"<br />
 - 99% of people who have this kind of surgery don't remember anything from the day of surgery.  I remember being positioned on the surgery table, and seeing the big surgery lights<br />
 - I got poked about a thousand times.  My favorite shots were insulin -- very little pain.  The worst were the heparin shots, which I got every 4 hours (including at night).  They feel like acid going in, and leave big ugly blue marks wherever you get one.  My stomach is polka dotted with them.<br />
 - Before the surgery, my fingernail beds were blue.  This is called cyanosis, and is a symptom of hypoxia (not enough oxygen).  I had assumed that they were blue just from being cold all the time, but immediately after the surgery they turned a nice healthy pink.<br />
