<p>Around April of 2006, I started experiencing heart palpitations.  I went in to the doctor, and he told me to get more exercise.  So I did (I took up cycling more seriously).  My resting heart rate fell to about 40-45 bpm.</p>

<p>On 11/18/08, I experienced an unusually strong palpitation.  I felt like I was almost blacking out, and my heart rate was really high.  I took an ambulance to PVH, where they ran blood tests.  They found nothing, and sent me home with a King of Hearts heart monitor.</p>

<p>As part of the followup for that, I went in to see a cardiologist.  He said he thought I just had a normal palpitation and hyperventilated, causing my other symptoms.  He also scheduled me for an echocardiogram to check for "extra tissue" that is apparently common in people my age, and can cause irregular heartbeats.</p>

<p>12/19/08: My echo shows this:</p>

